#include <stdio.h>
#include <stdbool.h>

int main() {
  // boolean is an integer, 0 or 1
  // 0 is false and 1 isi true
  printf("%i\n", true);
  printf("%d\n", 10 > 9);
  printf("%d\n", 10 == 10);
  printf("%d\n", 10 == 15);
  printf("%d\n", 5 == 55);
  printf("%d\n", true == 1);
  printf("%d\n", false == 0);
}
